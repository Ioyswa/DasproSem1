#include <iostream>
using namespace std;

int main() {
    int a = 10, b = 3;
    cout << "A = " << a << "\nB = " << b << endl;
    cout << "A + B = " << (a + b) << endl;
    cout << "A - B = " << (a - b) << endl;
    cout << "A * B = " << (a * b) << endl;
    cout << "A / B = " << (a / b) << " (Pembagian Bulat)" << endl << endl;
    double x = 10, y = 3;
    cout << "X = " << x << "\nY = " << y << endl;
    cout << "x / y = " << (x / y) << " (Pembagian Pecahan)" << endl;

}